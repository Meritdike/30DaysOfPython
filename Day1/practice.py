# string data type - If a string is more than one sentence then we use a triple quote.
'Asabeneh'
'Finland'
'Python'
'I love teaching'
'I hope you are enjoying the first day of 30DaysOfPython Challenge'

# boolean - F and T  is always in capital letter
True  #  Is the light on? If it is on, then the value is True
False # Is the light on? If it is off, then the value is False

# lists (ordered collection of data)- just like arrays in jaascript
[0, 1, 2, 3, 4, 5]  # all are the same data types - a list of numbers
['Banana', 'Orange', 'Mango', 'Avocado'] # all the same data types - a list of strings (fruits)
['Finland','Estonia', 'Sweden','Norway'] # all the same data types - a list of strings (countries)
['Banana', 10, False, 9.81] # different data types in the list - string, integer, boolean and float

# dictionary (unordered collection of data) - just like objects in javascript having key and value pairs
{
'first_name':'Asabeneh',
'last_name':'Yetayeh',
'country':'Finland', 
'age':250, 
'is_married':True,
'skills':['JS', 'React', 'Node', 'Python']
}

# Tuple - just like lists but connot be modified after creation (immutable)
('Asabeneh', 'Pawel', 'Brook', 'Abraham', 'Lidiya') # Names
('Earth', 'Jupiter', 'Neptune', 'Mars', 'Venus', 'Saturn', 'Uranus', 'Mercury') # planets

# Sets - just like javascript set stores unique data
{2, 4, 3, 5}
{3.14, 9.81, 2.7} # order is not important in set